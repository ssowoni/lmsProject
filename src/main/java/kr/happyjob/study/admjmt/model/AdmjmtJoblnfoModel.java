package kr.happyjob.study.admjmt.model;

public class AdmjmtJoblnfoModel {
	
	private int em_no;
	private String loginID;
	private String name;
	private String em_date;
	private String em_redate;
	private String em_nm;
	private String em_du;
	
	public int getEm_no() {
		return em_no;
	}
	public void setEm_no(int em_no) {
		this.em_no = em_no;
	}
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEm_date() {
		return em_date;
	}
	public void setEm_date(String em_date) {
		this.em_date = em_date;
	}
	public String getEm_redate() {
		return em_redate;
	}
	public void setEm_redate(String em_redate) {
		this.em_redate = em_redate;
	}
	public String getEm_nm() {
		return em_nm;
	}
	public void setEm_nm(String em_nm) {
		this.em_nm = em_nm;
	}
	public String getEm_du() {
		return em_du;
	}
	public void setEm_du(String em_du) {
		this.em_du = em_du;
	}
	

}
